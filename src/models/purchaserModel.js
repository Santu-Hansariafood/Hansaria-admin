const mongoose = require("mongoose");

const purchaserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    company: {
      type: String,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
    item: {
      type: String,
      required: true,
    },
    phoneNumber: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Purchaser = mongoose.model("Purchaser", purchaserSchema);

module.exports = Purchaser;
